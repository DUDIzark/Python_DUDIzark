##faca um programa que leia as duas notas de um aluno em uma materia e mostre na a sua media na disciplina

print('seja bem vindo a calculadora para professores!!')
print('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~')

n1=float(input('escreva a primeira nota do aluno: '))
n2=float(input('escreva a segunda nota do aluno: '))

media = (n1 + n2) / 2

print("media do aluno ficou: {}".format(media))